// iter 132 — one-shot propagator for hand-authored vertical templates
// (vertical:devops, :legal, :research, :support, :trading). Adds the
// .claude-plugin/plugin.json.tmpl that gen-templates.mjs already adds to
// the regen'd verticals, and inserts the file entry into manifest.json.
//
// Idempotent — re-runnable.
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { existsSync } from 'node:fs';

const TEMPLATES_ROOT = 'packages/create-agent-harness/templates';
const HAND_AUTHORED = ['vertical_devops', 'vertical_legal', 'vertical_research', 'vertical_support', 'vertical_trading'];
const uniq = (arr) => [...new Set(arr)];

function pluginJsonTmpl(id, domain) {
  const cats = uniq(['agent-harness', 'metaharness-scaffold', id.startsWith('vertical:') ? 'vertical' : 'category', domain]);
  const tags = uniq(['metaharness', 'agent-harness', id, domain]);
  return JSON.stringify({
    name: '{{name}}',
    version: '0.1.0',
    description: '{{description}}',
    author: { displayName: 'Generated by metaharness', url: 'https://www.npmjs.com/package/metaharness' },
    license: 'MIT',
    categories: cats,
    tags,
    homepage: 'https://github.com/ruvnet/agent-harness-generator',
  }, null, 2) + '\n';
}

for (const v of HAND_AUTHORED) {
  const dir = join(TEMPLATES_ROOT, v);
  const manifestPath = join(dir, 'manifest.json');
  if (!existsSync(manifestPath)) { console.error(`SKIP ${v}: no manifest`); continue; }
  const m = JSON.parse(await readFile(manifestPath, 'utf-8'));
  // Add plugin.json.tmpl file if missing
  const already = m.files.some((f) => f.src === '.claude-plugin/plugin.json.tmpl');
  if (!already) {
    // Insert after .claude/settings.json.tmpl
    const idx = m.files.findIndex((f) => f.src === '.claude/settings.json.tmpl');
    const entry = { src: '.claude-plugin/plugin.json.tmpl', dst: '.claude-plugin/plugin.json', render: true };
    if (idx >= 0) m.files.splice(idx + 1, 0, entry); else m.files.push(entry);
    await writeFile(manifestPath, JSON.stringify(m, null, 2) + '\n', 'utf-8');
    console.log(`  + ${v}/manifest.json: added .claude-plugin entry`);
  } else {
    console.log(`  = ${v}/manifest.json: already present`);
  }
  // Write the template body
  const tmplDir = join(dir, '.claude-plugin');
  await mkdir(tmplDir, { recursive: true });
  const tmplPath = join(tmplDir, 'plugin.json.tmpl');
  if (!existsSync(tmplPath)) {
    await writeFile(tmplPath, pluginJsonTmpl(m.id, m.domain ?? ''), 'utf-8');
    console.log(`  + ${v}/.claude-plugin/plugin.json.tmpl: written`);
  } else {
    console.log(`  = ${v}/.claude-plugin/plugin.json.tmpl: exists`);
  }
}
console.log('\n✓ propagated to all 5 hand-authored verticals');
